let str = 'My name is Bond, James Bond';

/**
 * New string methods
 */
console.log(str.startsWith('My')); // startsWith
console.log(str.endsWith('Bond')); // endsWith
console.log(str.includes('James')); // includes

/**
 * Numeral System
 */
console.log(0xFF); // hex
console.log(0b1010001); // binary
console.log(0o532); // octal

/**
 * New number methods
 */
console.log(Number.isFinite(NaN)); // isFinite
console.log(Number.isNaN('yahoo')); // isNan
console.log(Number.isInteger(Infinity)); // isInteger





